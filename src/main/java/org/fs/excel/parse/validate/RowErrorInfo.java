package org.fs.excel.parse.validate;

public class RowErrorInfo {
    private int row;
    private String msg;

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
